    /**
     * 将一个InputStream里面的数据写入SD卡中
     */
    public static File write2SDFromInput(String dir, String fileName,
                                         InputStream input) {
        File file = null;
        OutputStream output = null;
        try {
            creatSDDir(dir);
            file = creatSDFile(fileName, dir);
            output = new FileOutputStream(file);
            byte buffer[] = new byte[4 * 1024];
            int temp;
            while ((temp = input.read(buffer)) != -1) {
                output.write(buffer, 0, temp);
            }
            output.flush();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            if (output != null) {
                try {
                    output.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
        return file;
    }

